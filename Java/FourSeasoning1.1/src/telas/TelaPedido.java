    /**
     * metodo que finaliza o pedido, ele tambem atualiza o banco de dados, pois
     * eh necessario que fique registrado que a fatura do pedido nao foi paga ainda,
     * ela so podera ser paga na tela da conta
     * @param evt 
     */
    private void btnFinalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFinalizarActionPerformed
        tela.dispose();
        
        conta.setPago(false);
        if(conta.getCategoria().equals("Premium") || 
           conta.getCategoria().equals("Standard")){
            conta.setFatura(pedido.calcularTotalCDesconto() + conta.getFatura());
            
        }else{
            conta.setFatura(pedido.calcularTotal() + conta.getFatura());
        
        }
        TelaConta.gerente.marcarPonto();
        this.dispose();
    }//GEN-LAST:event_btnFinalizarActionPerformed
    /**
     * metodo para carregar a tabela com todos pedidos feitos pela conta
     */
    public void carregarTabela(){
        // Na versao anterior o DefaultTableModel estava declarado como global e isso causou erros
        DefaultTableModel modelo = new DefaultTableModel(new Object[]{"Nome do Prato",
                                   "Tamanho do Prato", "Quantidade", "Subtotal"},0);
        ArrayList<Pedido> pedidos = conta.getPedidos();
        for(Pedido p: pedidos){
            ArrayList<ItemPedido> items = p.getItemPedido();

            for(ItemPedido item: items){
                Object linha[] = new Object[]{item.getPrato().getNomePrato(), item.getTamanho(),
                                              item.getQtd(), String.format("%.2f", item.getSubtotal())};
                modelo.addRow(linha);
            }
        }
        tblPedido.setModel(modelo);
        
        if(conta.getCategoria().equals("Premium") || 
           conta.getCategoria().equals("Standard")){
            lblTotal.setForeground(Color.red);
            lblTotalCDesconto.setForeground(new Color(0,120,56));
            lblTotal.setText("Total: R$" + String.format("%.2f", pedido.calcularTotal()));
            lblTotalCDesconto.setText("Total ap√≥s desconto: R$" + String.format("%.2f", pedido.calcularTotalCDesconto()));
            
        }else{
            lblTotal.setForeground(new Color(0,120,56));
            lblTotal.setText("Total: R$" + String.format("%.2f", pedido.calcularTotal()));
            
        }
    }
