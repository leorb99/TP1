    /**
     * o botao confirmar tem um comportamento de acordo com o texto que esta definido nele
     * dependendo do texto exibido ele vai pesquisar uma conta, registrar uma conta 
     * ou remover uma conta
     * @param evt 
     */
    private void btnConfirmarCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarCActionPerformed
        if(btnConfirmarC.getText().equals("Pesquisar") || btnConfirmarC.getText().equals("Remover")){
            Conta c = gerente.pesquisarConta(txtCpfC.getText());
            if(c == null){
                lblFeedbackC.setForeground(Color.red);
                lblFeedbackC.setText("Conta não encontrada");
                lblFeedbackC.setVisible(true);
            
            }else if(btnConfirmarC.getText().equals("Pesquisar")){
                lstDadosC.setModel(new javax.swing.AbstractListModel<String>() {
                                  String[] dados = c.toString().split("\n");
                                  public int getSize() { return dados.length; }
                                  public String getElementAt(int i) { return dados[i]; }
                });
                jScrollPane1.setViewportView(lstDadosC);
                lstDadosC.setVisible(true);
                lblFeedbackC.setVisible(false);
                
            }else if(btnConfirmarC.getText().equals("Remover")){
                gerente.rmConta(c);
                lblFeedbackC.setForeground(Color.red);
                lblFeedbackC.setText("Conta removida");
                lblFeedbackC.setVisible(true);
                
        }
        }else if(btnConfirmarC.getText().equals("Adicionar")){
            
            String senha = new String(txtSenhaC.getPassword());
            String confirmarSenha = new String(txtCSenhaC.getPassword());
            
            if(txtNomeC.getText().equals("") || txtCpfC.getText().equals("") ||
               txtTelefoneC.getText().equals("") || txtEmailC.getText().equals("") ||
               senha.equals("") || confirmarSenha.equals("") || txtDepositoC.getText().equals("") ||
               cbbCategoriaC.getSelectedIndex() == 0 || cbbGeneroC.getSelectedIndex() == 0){
                
                lblFeedbackC.setText("Preencha todos campos");    
                lblFeedbackC.setForeground(Color.red);
                lblFeedbackC.setVisible(true);
                
            }else if(!senha.equals(confirmarSenha)){
                lblFeedbackC.setText("Senhas não correspondem");    
                lblFeedbackC.setForeground(Color.red);
                lblFeedbackC.setVisible(true);
            }
            else{
                Conta novaConta = new Conta();
                novaConta.setNome(txtNomeC.getText());
                novaConta.setCpf(txtCpfC.getText());
                novaConta.setTelefone(txtTelefoneC.getText());
                novaConta.setEmail(txtEmailC.getText());
                novaConta.setSenha(senha);
                novaConta.setSaldo(Double.parseDouble(txtDepositoC.getText()));
                novaConta.setCategoria(cbbCategoriaC.getSelectedItem().toString());
                novaConta.setGenero(cbbGeneroC.getSelectedItem().toString().toLowerCase().charAt(0));
                novaConta.setIdConta(getContas().get(getContas().size()-1).getIdConta() + 1);
                
                gerente.addConta(novaConta);
                
                lblFeedbackC.setText("Conta cadastrada");    
                lblFeedbackC.setForeground(new java.awt.Color(0,120,56));
                lblFeedbackC.setVisible(true);
            }
        }
        
    }//GEN-LAST:event_btnConfirmarCActionPerformed

