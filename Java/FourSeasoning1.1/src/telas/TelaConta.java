    /**
     * metodo onde a conta pode ser excluida
     * @param evt 
     */
    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        String senha = JOptionPane.showInputDialog(null, "Digite sua senha", "Tem certeza?", JOptionPane.QUESTION_MESSAGE);
        if(conta.isSenha(senha)){
            gerente.rmConta(conta);
            JOptionPane.showMessageDialog(null, "Conta removida", "Aviso", JOptionPane.WARNING_MESSAGE);
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(null, "Senha incorreta", "Aviso", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnExcluirActionPerformed
