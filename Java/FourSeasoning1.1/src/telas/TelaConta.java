    /**
     * metodo para registrar uma nova conta
     * @param evt 
     */
    private void btnConfirmarNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarNActionPerformed
        String senha = new String(txtSenhaN.getPassword());
        String confirmarSenha = new String(txtCSenhaN.getPassword());
    
        if(txtNomeN.getText().equals("") || txtCpfN.getText().equals("") ||
            txtTelefoneN.getText().equals("") || txtEmailN.getText().equals("") ||
            senha.equals("") || confirmarSenha.equals("") || txtDepositoN.getText().equals("") ||
            cbbCategoriaN.getSelectedIndex() == 0 || cbbGeneroN.getSelectedIndex() == 0){
            
            lblErroN.setText("Preencha todos campos");    
            lblErroN.setForeground(Color.red);
            lblErroN.setVisible(true);
                
        }else if(!senha.equals(confirmarSenha)){
            lblErroN.setText("Senhas não correspondem");    
            lblErroN.setForeground(Color.red);
            lblErroN.setVisible(true);
            
        }else if(cbbCategoriaN.getSelectedIndex() == 2 && Double.parseDouble(txtDepositoN.getText()) < 119.9){
            lblErroN.setVisible(true);
            lblErroN.setText("Depósito insuficiente. Depósito mínimo R$119.90");
            
        }else if(cbbCategoriaN.getSelectedIndex() == 3 && Double.parseDouble(txtDepositoN.getText()) < 199.9){
            lblErroN.setVisible(true);
            lblErroN.setText("Depósito insuficiente. Depósito mínimo R$199.90");
            
        }else if(gerente.pesquisarConta(txtCpfN.getText()) != null){
            lblErroN.setVisible(true);
            lblErroN.setText("Conta já existente");
            
        }
        else{
            Conta novaConta = new Conta();
            novaConta.setNome(txtNomeN.getText());
            novaConta.setCpf(txtCpfN.getText());
            novaConta.setTelefone(txtTelefoneN.getText());
            novaConta.setEmail(txtEmailN.getText());
            novaConta.setSenha(senha);
            novaConta.setSaldo(Double.parseDouble(txtDepositoN.getText()));
            novaConta.setCategoria(cbbCategoriaN.getSelectedItem().toString());
            novaConta.setGenero(cbbGeneroN.getSelectedItem().toString().toLowerCase().charAt(0));
            novaConta.setIdConta(getContas().get(getContas().size()-1).getIdConta() + 1);
            novaConta.setPago(true);
            // definindo a mesa da nova conta
            novaConta.setMesa(new Mesa((byte)0));
            gerente.addConta(novaConta);
            conta = novaConta;
            
            if(!conta.isPago()){
                    btnPedido.setEnabled(false);
                    btnPedido.setToolTipText("Pague a fatura");
            }else{
                    btnPedido.setEnabled(true);
                    btnPedido.setToolTipText("Fazer pedido");
            }
            
            pnlNovaConta.setVisible(false);
            lpnConta.setVisible(true);
            pnlConta.setVisible(true);
            
            lblTitulo.setText("Olá, " + conta.getNome().split(" ")[0]);
        }
    }//GEN-LAST:event_btnConfirmarNActionPerformed

