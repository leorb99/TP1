    /**
     * metodo que faz o login na conta apos fazer algumas verificacoes
     * @param evt 
     */
    private void btnLoginLActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginLActionPerformed
        String senha = new String(txtSenhaL.getPassword());
        String cpf = txtCpfL.getText();
        boolean flag = false;
       
        
        for(Conta c: contas){
            if(c.getCpf().equals(cpf) && c.isSenha(senha)){
                conta = c;
                pnlLogin.setVisible(false);
                lpnConta.setVisible(true);
                pnlConta.setVisible(true);
                flag = true;
                lblTitulo.setText("Olá, " + conta.getNome().split(" ")[0]);
                if(!conta.isPago()){
                    btnPedido.setEnabled(false);
                    btnPedido.setToolTipText("Pague a fatura");
                }else{
                    btnPedido.setEnabled(true);
                    btnPedido.setToolTipText("Fazer pedido");
                }
                
                break;
            }
        }

        if(!conta.isPago()){
            btnPagar.setVisible(true);
            lblPagar.setVisible(true);
            lblPagar.setText("Total a pagar: R$" + String.format("%.2f",conta.getFatura()));
        }else{
            btnPagar.setVisible(false);
            lblPagar.setText("");
        }
        
        if(!flag){
            lblErroL.setVisible(true);
            txtCpfL.setText("");
            txtSenhaL.setText("");
        }
    }//GEN-LAST:event_btnLoginLActionPerformed
